package operator

import (
	"encoding/json"
	"fmt"
)

func isNumeric(val interface{}) bool {
	switch val.(type) {
	case int, int8, int16, int32, int64, uint, uint8, uint16, uint32, uint64, float32, float64, json.Number:
		return true
	}
	return false
}

func toFloat64(val interface{}) float64 {
	switch v := val.(type) {
	case int:
		return float64(v)
	case int8:
		return float64(v)
	case int16:
		return float64(v)
	case int32:
		return float64(v)
	case int64:
		return float64(v)
	case uint:
		return float64(v)
	case uint8:
		return float64(v)
	case uint16:
		return float64(v)
	case uint32:
		return float64(v)
	case uint64:
		return float64(v)
	case json.Number:
		val, err := val.(json.Number).Float64()
		if err != nil {
			panic(fmt.Sprintf("type is not float64, error: %v", err))
		}
		return val
	case float64:
		return v
	case float32:
		return float64(v)
	default:
		panic(fmt.Sprintf("unsupported type, value: %v", val))

	}
}
